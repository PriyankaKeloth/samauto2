package automationcourse;

public class Staticpara {
	public static void main (String args[]) {

		Staticpara.add();
		Staticpara.sub();
	}
	public static void add()
	{
	int a=5;
	int b=2;
	int d=a+b;
	System.out.println(d);
	}
	public static void sub()
	{
	int b=2;
	int c=7;
	int e=c-b;
	System.out.println(e);
	}
	
}

